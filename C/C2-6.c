#include <stdio.h>

int main() {
  int a = 10, b = 3;

  printf("a + b = %d\n", a + b);  // Addition
  printf("a - b = %d\n", a - b);  // Subtraction
  printf("a * b = %d\n", a * b);  // Multiplication
  printf("a / b = %d\n", a / b);  // Division
  printf("a %% b = %d\n", a % b); // Modulus (remainder)

  return 0;
}
